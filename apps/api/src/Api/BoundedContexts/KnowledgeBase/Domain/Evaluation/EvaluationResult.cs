namespace Api.BoundedContexts.KnowledgeBase.Domain.Evaluation;

/// <summary>
/// Result of evaluating a single sample in the RAG pipeline.
/// </summary>
public sealed record EvaluationSampleResult
{
    /// <summary>
    /// Sample ID that was evaluated.
    /// </summary>
    public required string SampleId { get; init; }

    /// <summary>
    /// The question asked.
    /// </summary>
    public required string Question { get; init; }

    /// <summary>
    /// Expected correct answer.
    /// </summary>
    public required string ExpectedAnswer { get; init; }

    /// <summary>
    /// Actual answer generated by RAG system.
    /// </summary>
    public string? GeneratedAnswer { get; init; }

    /// <summary>
    /// IDs of chunks retrieved by the system.
    /// </summary>
    public IReadOnlyList<string> RetrievedChunkIds { get; init; } = [];

    /// <summary>
    /// IDs of relevant chunks that should have been retrieved.
    /// </summary>
    public IReadOnlyList<string> RelevantChunkIds { get; init; } = [];

    /// <summary>
    /// Reciprocal rank of first relevant chunk (1/position, 0 if not found).
    /// </summary>
    public double ReciprocalRank { get; init; }

    /// <summary>
    /// Whether any relevant chunk was retrieved in top 5.
    /// </summary>
    public bool HitAt5 { get; init; }

    /// <summary>
    /// Whether any relevant chunk was retrieved in top 10.
    /// </summary>
    public bool HitAt10 { get; init; }

    /// <summary>
    /// DCG score at position 10.
    /// </summary>
    public double DcgAt10 { get; init; }

    /// <summary>
    /// Ideal DCG score at position 10.
    /// </summary>
    public double IdealDcgAt10 { get; init; }

    /// <summary>
    /// Answer correctness score from keyword matching or LLM judge.
    /// </summary>
    public double AnswerCorrectness { get; init; }

    /// <summary>
    /// Latency in milliseconds for this query.
    /// </summary>
    public double LatencyMs { get; init; }

    /// <summary>
    /// Confidence score from RAG system.
    /// </summary>
    public double Confidence { get; init; }

    /// <summary>
    /// Error message if evaluation failed.
    /// </summary>
    public string? ErrorMessage { get; init; }

    /// <summary>
    /// Whether evaluation completed successfully.
    /// </summary>
    public bool IsSuccess => string.IsNullOrEmpty(ErrorMessage);

    /// <summary>
    /// nDCG at position 10 (DcgAt10 / IdealDcgAt10).
    /// </summary>
    public double NdcgAt10 => IdealDcgAt10 > 0 ? DcgAt10 / IdealDcgAt10 : 0.0;
}

/// <summary>
/// Complete evaluation result for a dataset run.
/// </summary>
public sealed record EvaluationResult
{
    /// <summary>
    /// Unique identifier for this evaluation run.
    /// </summary>
    public required string EvaluationId { get; init; }

    /// <summary>
    /// Dataset name that was evaluated.
    /// </summary>
    public required string DatasetName { get; init; }

    /// <summary>
    /// Configuration used for this evaluation.
    /// </summary>
    public required string Configuration { get; init; }

    /// <summary>
    /// Timestamp when evaluation started.
    /// </summary>
    public DateTime StartedAt { get; init; }

    /// <summary>
    /// Timestamp when evaluation completed.
    /// </summary>
    public DateTime CompletedAt { get; init; }

    /// <summary>
    /// Individual sample results.
    /// </summary>
    public IReadOnlyList<EvaluationSampleResult> SampleResults { get; init; } = [];

    /// <summary>
    /// Aggregated metrics across all samples.
    /// </summary>
    public EvaluationMetrics Metrics { get; init; } = EvaluationMetrics.Empty;

    /// <summary>
    /// Metrics broken down by difficulty.
    /// </summary>
    public IReadOnlyDictionary<string, EvaluationMetrics> MetricsByDifficulty { get; init; } =
        new Dictionary<string, EvaluationMetrics>(StringComparer.Ordinal);

    /// <summary>
    /// Metrics broken down by category.
    /// </summary>
    public IReadOnlyDictionary<string, EvaluationMetrics> MetricsByCategory { get; init; } =
        new Dictionary<string, EvaluationMetrics>(StringComparer.Ordinal);

    /// <summary>
    /// Metrics broken down by game.
    /// </summary>
    public IReadOnlyDictionary<string, EvaluationMetrics> MetricsByGame { get; init; } =
        new Dictionary<string, EvaluationMetrics>(StringComparer.Ordinal);

    /// <summary>
    /// Total duration of evaluation in milliseconds.
    /// </summary>
    public double TotalDurationMs => (CompletedAt - StartedAt).TotalMilliseconds;

    /// <summary>
    /// Number of successful evaluations.
    /// </summary>
    public int SuccessCount => SampleResults.Count(r => r.IsSuccess);

    /// <summary>
    /// Number of failed evaluations.
    /// </summary>
    public int FailureCount => SampleResults.Count(r => !r.IsSuccess);

    /// <summary>
    /// Creates a new evaluation result from sample results.
    /// </summary>
    public static EvaluationResult Create(
        string datasetName,
        string configuration,
        DateTime startedAt,
        DateTime completedAt,
        IReadOnlyList<EvaluationSampleResult> sampleResults)
    {
        var metrics = CalculateMetrics(sampleResults);
        var metricsByDifficulty = CalculateMetricsByGroup(sampleResults, getDifficulty: true);
        var metricsByCategory = CalculateMetricsByGroup(sampleResults, getDifficulty: false);

        return new EvaluationResult
        {
            EvaluationId = Guid.NewGuid().ToString(),
            DatasetName = datasetName,
            Configuration = configuration,
            StartedAt = startedAt,
            CompletedAt = completedAt,
            SampleResults = sampleResults,
            Metrics = metrics,
            MetricsByDifficulty = metricsByDifficulty,
            MetricsByCategory = metricsByCategory
        };
    }

    private static EvaluationMetrics CalculateMetrics(IReadOnlyList<EvaluationSampleResult> results)
    {
        if (results.Count == 0)
        {
            return EvaluationMetrics.Empty;
        }

        var successfulResults = results.Where(r => r.IsSuccess).ToList();

        if (successfulResults.Count == 0)
        {
            return EvaluationMetrics.Empty;
        }

        var recallAt5 = successfulResults.Average(r => r.HitAt5 ? 1.0 : 0.0);
        var recallAt10 = successfulResults.Average(r => r.HitAt10 ? 1.0 : 0.0);
        var ndcgAt10 = successfulResults.Average(r => r.NdcgAt10);
        var mrr = successfulResults.Average(r => r.ReciprocalRank);
        var answerCorrectness = successfulResults.Average(r => r.AnswerCorrectness);

        // Calculate P95 latency
        var latencies = successfulResults.Select(r => r.LatencyMs).OrderBy(l => l).ToList();
        var p95Index = (int)Math.Ceiling(latencies.Count * 0.95) - 1;
        var p95Latency = latencies.Count > 0 ? latencies[Math.Max(0, p95Index)] : 0.0;

        return EvaluationMetrics.Create(
            recallAt5: recallAt5,
            recallAt10: recallAt10,
            ndcgAt10: ndcgAt10,
            mrr: mrr,
            p95LatencyMs: p95Latency,
            answerCorrectness: answerCorrectness,
            sampleCount: successfulResults.Count
        );
    }

    private static IReadOnlyDictionary<string, EvaluationMetrics> CalculateMetricsByGroup(
                )
    {
        // Note: This is a simplified implementation. In practice, you'd need to
        // join with the original samples to get difficulty/category metadata.
        // For now, returning empty as the sample results don't carry this metadata.
        return new Dictionary<string, EvaluationMetrics>(StringComparer.Ordinal);
    }
}
